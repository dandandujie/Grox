export type TranslationKey = keyof typeof zh;

export function useI18n() {
  const language = usePreferences((state) => state.language);
  const t = useCallback(
    (key: TranslationKey) => (language === "zh-CN" ? zh[key] : en[key]),
    [language],
  );
  return { language, t };
}
